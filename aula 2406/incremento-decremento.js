// Incremento -> Somar de 1 em 1
// Decremento -> Subitrair de 1 em 1

let a = 0;
a += 1; // 0 + 1 = 1, o novo valor de "a" é 1
a += 1; // 1 + 1 = 2, o novo valor de "a" é 2
a += 1; // 2 + 1 = 3, o novo valor de "a" é 3
console.log(a);

let idade = 18;
